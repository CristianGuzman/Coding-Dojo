package com.codingdojo.guardia;

public class Mammal {
	
	Integer Energia = 100;

	
	
	/**
	 * 
	 */
	public Mammal() {
		super();
	}

	/**
	 * @param energia
	 */
	public Mammal(Integer energia) {
		super();
		Energia = energia;
	}

	/**
	 * @return the energia
	 */
	public Integer getEnergia() {
		return Energia;
	}

	/**
	 * @param energia the energia to set
	 */
	public void setEnergia(Integer energia) {
		Energia = energia;
	}
	
	

}
